# Positive or negative
a = 1

if a == 0:
    print('It is zero')
elif a > 0:
    print('It is positive')
    print('It is really positive')
    print('I mean it!')
else:
    print('It is negative')
    print('It is really negative')
    print('I mean it!')


# Even or odd
n = 0

if n % 2 == 0:
    print('even')
elif n % 2 == 1:
    print('odd')
else:
    print('not an integer')
